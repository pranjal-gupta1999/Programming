class math_function
{
	public static void main(String ar[])
	{
		System.out.println(Math.random());  //generates no. between 0 and 1.
		System.out.println(Math.round(11.3));
		System.out.println(Math.min(11,16));
		System.out.println(Math.max(10,24));
		System.out.println(Math.ceil(11.23));  // next int value
		System.out.println(Math.floor(11.55));  // same int value
		System.out.println(Math.pow(2,4));
		System.out.println(Math.sqrt(36));
		System.out.println(Math.cbrt(64));
	}
}